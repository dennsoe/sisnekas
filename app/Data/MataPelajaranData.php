    public static function getAllMataPelajaran(): array
    {
        $allMapel = [];
        
        // Fungsi untuk meratakan array bertingkat menjadi satu level
        $flatten = function($array) use (&$flatten, &$allMapel) {
            foreach ($array as $key => $value) {
                if (is_array($value)) {
                    $flatten($value);
                } else {
                    $allMapel[$value] = $value;
                }
            }
        };

        // Gabungkan semua mata pelajaran dari setiap jenjang
        $flatten(self::getMataPelajaranSD());
        $flatten(self::getMataPelajaranSMP());
        $flatten(self::getMataPelajaranSMA());
        $flatten(self::getMataPelajaranSMK());

        // Urutkan berdasarkan abjad
        ksort($allMapel);

        return $allMapel;
    }

    public static function getMataPelajaranByJenjang(string $jenjang): array
    {
        return match ($jenjang) {
            'SD' => self::getMataPelajaranSD(),
            'SMP' => self::getMataPelajaranSMP(),
            'SMA' => self::getMataPelajaranSMA(),
            'SMK' => self::getMataPelajaranSMK(),
            default => [],
        };
    }
