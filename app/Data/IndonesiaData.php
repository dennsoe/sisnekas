    public static function getAllCities(): array
    {
        $cities = [];
        foreach (self::getKota() as $province => $cityList) {
            foreach ($cityList as $city) {
                $cities[$city] = $city;
            }
        }
        return $cities;
    }

    public static function getCitiesGroupedByProvince(): array
    {
        $groupedCities = [];
        foreach (self::getKota() as $province => $cities) {
            foreach ($cities as $city) {
                $groupedCities[$province][$city] = $city;
            }
        }
        return $groupedCities;
    }
